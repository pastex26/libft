#include "libft.h"

char	*ft_strncat(char *dest, const char *src, size_t n)
{
	size_t	i;
	size_t	j;

	if (!src || !dest)
		return (dest);
	i = ft_strlen(dest);
	j = 0;
	if (!dest)
		return (NULL);
	if (!src || n <= 0)
		return (dest);
	while (src[j] && j < n)
	{
		dest[i] = src[j];
		j++;
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
